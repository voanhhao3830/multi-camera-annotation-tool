# Visualize kết quả như video - từng frame một
def visualize_tracking_video(tracker, global_ids, fps=2.0, save_gif=False):
    """
    Visualize tracking results như video - hiển thị từng frame tuần tự
    
    Args:
        tracker: ObjectTracking instance
        global_ids: Dict với key (frame_idx, obj_idx) và value là global_id
        fps: Frames per second cho animation
        save_gif: Nếu True, lưu thành GIF
    """
    import matplotlib.animation as animation
    from matplotlib.patches import Circle
    
    # Lấy số lượng unique global IDs
    unique_ids = sorted(set(global_ids.values()))
    num_ids = len(unique_ids)
    
    # Tạo colormap
    cmap = plt.get_cmap('tab20' if num_ids <= 20 else 'viridis')
    colors = [cmap(i / max(1, num_ids - 1)) for i in range(num_ids)]
    id_to_color = {unique_ids[i]: colors[i] for i in range(num_ids)}
    
    # Tính toán bounds để set axis limits
    all_x = []
    all_y = []
    for frame_objects in tracker.list_of_labels:
        for x, y in frame_objects:
            all_x.append(x)
            all_y.append(y)
    
    if not all_x or not all_y:
        print("No data to visualize")
        return
    
    x_min, x_max = min(all_x), max(all_x)
    y_min, y_max = min(all_y), max(all_y)
    x_range = x_max - x_min
    y_range = y_max - y_min
    
    # Thêm margin
    margin = max(x_range, y_range) * 0.1
    x_min -= margin
    x_max += margin
    y_min -= margin
    y_max += margin
    
    # Tạo figure
    fig, ax = plt.subplots(figsize=(12, 10))
    ax.set_xlim(x_min, x_max)
    ax.set_ylim(y_min, y_max)
    ax.set_aspect('equal')
    ax.grid(True, alpha=0.3)
    ax.set_xlabel('X Position', fontsize=12)
    ax.set_ylabel('Y Position', fontsize=12)
    
    # Vẽ trajectories nền (mờ) để thấy đường đi
    for global_id in unique_ids:
        points = []
        for (frame_idx, obj_idx), gid in global_ids.items():
            if gid == global_id:
                x, y = tracker.list_of_labels[frame_idx][obj_idx]
                points.append((frame_idx, x, y))
        
        points.sort(key=lambda p: p[0])
        if len(points) > 1:
            xs = [p[1] for p in points]
            ys = [p[2] for p in points]
            ax.plot(xs, ys, '--', color=id_to_color[global_id], 
                   linewidth=1, alpha=0.2, zorder=1)
    
    # Danh sách để lưu các elements cần update
    scatter_plots = []
    text_annotations = []
    frame_text = None
    status_text = None
    
    # Biến điều khiển
    current_frame = [0]  # Dùng list để có thể modify trong nested functions
    is_paused = [False]  # Trạng thái pause
    num_frames = len(tracker.list_of_labels)
    
    def draw_frame(frame_idx):
        """Vẽ một frame cụ thể"""
        nonlocal frame_text, status_text
        
        # Xóa các elements cũ
        for scat in scatter_plots:
            scat.remove()
        scatter_plots.clear()
        
        for txt in text_annotations:
            txt.remove()
        text_annotations.clear()
        
        if frame_text:
            frame_text.remove()
        if status_text:
            status_text.remove()
        
        # Giới hạn frame index
        if frame_idx < 0:
            frame_idx = 0
        if frame_idx >= num_frames:
            frame_idx = num_frames - 1
        
        # Lấy objects ở frame hiện tại
        frame_objects = tracker.list_of_labels[frame_idx]
        
        # Vẽ các điểm ở frame hiện tại
        xs = []
        ys = []
        colors_list = []
        sizes = []
        labels = []
        
        for obj_idx, (x, y) in enumerate(frame_objects):
            global_id = global_ids.get((frame_idx, obj_idx), -1)
            if global_id >= 0:
                xs.append(x)
                ys.append(y)
                colors_list.append(id_to_color[global_id])
                sizes.append(300)
                labels.append(f'ID: {global_id}')
        
        if xs:
            # Vẽ scatter plot
            scatter = ax.scatter(xs, ys, c=colors_list, s=sizes, 
                                edgecolors='black', linewidths=2, 
                                alpha=0.8, zorder=3)
            scatter_plots.append(scatter)
            
            # Thêm text labels
            for i, (x, y, label) in enumerate(zip(xs, ys, labels)):
                txt = ax.annotate(label, (x, y), 
                                xytext=(0, 15), textcoords='offset points',
                                fontsize=12, fontweight='bold',
                                ha='center',
                                bbox=dict(boxstyle='round,pad=0.5', 
                                        facecolor='white', alpha=0.9, 
                                        edgecolor=colors_list[i], linewidth=2),
                                zorder=4)
                text_annotations.append(txt)
        
        # Hiển thị frame number
        frame_text = ax.text(0.02, 0.98, f'Frame: {frame_idx}/{num_frames-1}', 
                           transform=ax.transAxes, fontsize=14, fontweight='bold',
                           verticalalignment='top',
                           bbox=dict(boxstyle='round', facecolor='yellow', alpha=0.8),
                           zorder=5)
        
        # Hiển thị trạng thái
        status = "PAUSED" if is_paused[0] else "PLAYING"
        status_text = ax.text(0.98, 0.98, f'Status: {status}', 
                            transform=ax.transAxes, fontsize=12, fontweight='bold',
                            verticalalignment='top', horizontalalignment='right',
                            bbox=dict(boxstyle='round', facecolor='lightblue', alpha=0.8),
                            zorder=5)
        
        ax.set_title(f'Object Tracking - Frame {frame_idx}', 
                    fontsize=16, fontweight='bold', pad=20)
        
        return scatter_plots + text_annotations + ([frame_text] if frame_text else []) + ([status_text] if status_text else [])
    
    def animate(frame_idx):
        """Update frame cho animation"""
        if is_paused[0]:
            # Nếu đang pause, giữ nguyên frame hiện tại
            draw_frame(current_frame[0])
        else:
            # Nếu đang play, cập nhật frame
            current_frame[0] = frame_idx
            draw_frame(current_frame[0])
        return scatter_plots + text_annotations + ([frame_text] if frame_text else []) + ([status_text] if status_text else [])
    
    # Event handlers cho keyboard
    def on_key_press(event):
        """Xử lý phím bấm"""
        if event.key == ' ':  # Space - Pause/Resume
            is_paused[0] = not is_paused[0]
            draw_frame(current_frame[0])
            fig.canvas.draw()
        
        elif event.key in ['left', 'a', 'A']:  # Frame trước
            is_paused[0] = True  # Tự động pause khi điều khiển thủ công
            current_frame[0] = max(0, current_frame[0] - 1)
            draw_frame(current_frame[0])
            fig.canvas.draw()
        
        elif event.key in ['right', 'd', 'D']:  # Frame sau
            is_paused[0] = True  # Tự động pause khi điều khiển thủ công
            current_frame[0] = min(num_frames - 1, current_frame[0] + 1)
            draw_frame(current_frame[0])
            fig.canvas.draw()
        
        elif event.key == 'escape':  # ESC - Thoát
            plt.close(fig)
    
    # Kết nối event handler
    fig.canvas.mpl_connect('key_press_event', on_key_press)
    
    # Vẽ frame đầu tiên
    draw_frame(0)
    
    # Tạo animation
    interval = int(1000 / fps)  # milliseconds
    
    anim = animation.FuncAnimation(fig, animate, frames=num_frames, 
                                  interval=interval, repeat=True, blit=False)
    
    # Hiển thị hướng dẫn
    print("\n" + "="*60)
    print("KEYBOARD CONTROLS:")
    print("  SPACE     - Pause/Resume")
    print("  LEFT / A  - Previous frame")
    print("  RIGHT / D - Next frame")
    print("  ESC       - Exit")
    print("="*60 + "\n")
    
    # Lưu GIF nếu được yêu cầu
    if save_gif:
        print("Saving GIF...")
        anim.save('tracking_animation.gif', writer='pillow', fps=fps)
        print("GIF saved as 'tracking_animation.gif'")
    
    plt.tight_layout()
    plt.show()
    
    return anim

# Gọi hàm visualize như video
anim = visualize_tracking_video(tracker, global_ids, fps=2.0, save_gif=False)